import java.util.List;

public class PrimitiveLists {

    private List<Boolean> boolList;

    private List<Byte> byteList;

    private List<Short> shortList;

    private List<Integer> intList;

    private List<Long> longList;

    private List<Float> floatList;

    private List<Double> doubleList;

    private List<Character> charList;

    public List<Boolean> getBoolList() {
        return boolList;
    }

    public void setBoolList(final List<Boolean> boolList) {
        this.boolList = boolList;
    }

    public List<Byte> getByteList() {
        return byteList;
    }

    public void setByteList(final List<Byte> byteList) {
        this.byteList = byteList;
    }

    public List<Short> getShortList() {
        return shortList;
    }

    public void setShortList(final List<Short> shortList) {
        this.shortList = shortList;
    }

    public List<Integer> getIntList() {
        return intList;
    }

    public void setIntList(final List<Integer> intList) {
        this.intList = intList;
    }

    public List<Long> getLongList() {
        return longList;
    }

    public void setLongList(final List<Long> longList) {
        this.longList = longList;
    }

    public List<Float> getFloatList() {
        return floatList;
    }

    public void setFloatList(final List<Float> floatList) {
        this.floatList = floatList;
    }

    public List<Double> getDoubleList() {
        return doubleList;
    }

    public void setDoubleList(final List<Double> doubleList) {
        this.doubleList = doubleList;
    }

    public List<Character> getCharList() {
        return charList;
    }

    public void setCharList(final List<Character> charList) {
        this.charList = charList;
    }
}
