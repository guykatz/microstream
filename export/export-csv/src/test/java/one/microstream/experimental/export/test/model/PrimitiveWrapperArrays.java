public class PrimitiveWrapperArrays {

    private Boolean[] boolArray;

    private Byte[] byteArray;

    private Short[] shortArray;

    private Integer[] intArray;

    private Long[] longArray;

    private Float[] floatArray;

    private Double[] doubleArray;

    private Character[] charArray;

    public Boolean[] getBoolArray() {
        return boolArray;
    }

    public void setBoolArray(final Boolean[] boolArray) {
        this.boolArray = boolArray;
    }

    public Byte[] getByteArray() {
        return byteArray;
    }

    public void setByteArray(final Byte[] byteArray) {
        this.byteArray = byteArray;
    }

    public Short[] getShortArray() {
        return shortArray;
    }

    public void setShortArray(final Short[] shortArray) {
        this.shortArray = shortArray;
    }

    public Integer[] getIntArray() {
        return intArray;
    }

    public void setIntArray(final Integer[] intArray) {
        this.intArray = intArray;
    }

    public Long[] getLongArray() {
        return longArray;
    }

    public void setLongArray(final Long[] longArray) {
        this.longArray = longArray;
    }

    public Float[] getFloatArray() {
        return floatArray;
    }

    public void setFloatArray(final Float[] floatArray) {
        this.floatArray = floatArray;
    }

    public Double[] getDoubleArray() {
        return doubleArray;
    }

    public void setDoubleArray(final Double[] doubleArray) {
        this.doubleArray = doubleArray;
    }

    public Character[] getCharArray() {
        return charArray;
    }

    public void setCharArray(final Character[] charArray) {
        this.charArray = charArray;
    }
}
