public class PrimitiveWrappers {

    private Boolean aBool;

    private Byte aByte;

    private Short aShort;

    private Integer anInt;

    private Long aLong;

    private Float aFloat;

    private Double aDouble;

    private Character aCharacter;

    public Boolean getaBool() {
        return aBool;
    }

    public void setaBool(final Boolean aBool) {
        this.aBool = aBool;
    }

    public Byte getaByte() {
        return aByte;
    }

    public void setaByte(final Byte aByte) {
        this.aByte = aByte;
    }

    public Short getaShort() {
        return aShort;
    }

    public void setaShort(final Short aShort) {
        this.aShort = aShort;
    }

    public Integer getAnInt() {
        return anInt;
    }

    public void setAnInt(final Integer anInt) {
        this.anInt = anInt;
    }

    public Long getaLong() {
        return aLong;
    }

    public void setaLong(final Long aLong) {
        this.aLong = aLong;
    }

    public Float getaFloat() {
        return aFloat;
    }

    public void setaFloat(final Float aFloat) {
        this.aFloat = aFloat;
    }

    public Double getaDouble() {
        return aDouble;
    }

    public void setaDouble(final Double aDouble) {
        this.aDouble = aDouble;
    }

    public Character getaCharacter() {
        return aCharacter;
    }

    public void setaCharacter(final Character aCharacter) {
        this.aCharacter = aCharacter;
    }
}
